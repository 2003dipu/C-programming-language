#include<stdio.h>


int main()
{
    // this is a single line comment
    /*
    this is 
    a
    multiline
    comment
    */
   /*
   escape sequence = character combination consisting of a backslash \ followed by a letter or combination of
   digits. They specify actions within a line or string of text.
   \n = newline
   \t = tab
   */
    printf("\n\nHello World\n");
    printf("\n\nI like Chambhut!\n");
    printf("This is a grid of number\n\n"); 
    printf("1\t2\t3\n4\t5\t6\n7\t8\t9\n0\n");
    printf("\" I like Pizza\" - Rosmi Sinha \n\n"); // print double quotes ""
    printf("\' I like Pizza\' - Rosmi Sinha \n\n"); // print single quotes ''
    printf("\\ I like Pizza\\ - Rosmi Sinha \n\n"); // print backward slash quotes \
    return 0;
    
}
