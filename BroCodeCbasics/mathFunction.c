#include<math.h>
#include<stdio.h>

int main()
{
    double A = sqrt(9);
    printf("%lf \n", A);

    double B = pow(A, 2);
    printf("%lf \n", B);

    double C = ceil(3.14159);
    printf("%lf \n", C);

    double D = floor(3.14159);
    printf("%lf \n",D);

    int E = fabs(-102);
    printf("%lf \n", E);

    double F = log(3);
    printf("%lf \n", F);

    double G = sin(45);
    printf("%lf \n", G);

    double H = cos(45);
    printf("%lf \n", H);

    double I = tan(45);
    printf("%lf \n", I);


    return 0;
}